public class Vendas {
        int quantidade;
    double precoUnitario;
    double valorFinal;
    int dia;
    int mes;
    int ano;

    public Vendas(int quantidade, double precoUnitario, double valorFinal, int dia, int mes, int ano) {
        this.quantidade = quantidade;
        this.precoUnitario = precoUnitario;
        this.valorFinal = valorFinal;
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }
}
